package transferobjects;

public class Tuna {
    private Integer tunaId;
    private String genusName;
    private String speciesName;
    private String commonName;
    private double maxWeight;
    private String iucnStatus;

    public Integer getTunaId() {
        return tunaId;
    }

    public void setTunaId(Integer tunaId) {
        this.tunaId = tunaId;
    }

    public String getGenusName() {
        return genusName;
    }

    public void setGenusName(String genusName) {
        this.genusName = genusName;
    }

    public String getSpeciesName() {
        return speciesName;
    }

    public void setSpeciesName(String speciesName) {
        this.speciesName = speciesName;
    }

    public String getCommonName() {
        return commonName;
    }

    public void setCommonName(String commonName) {
        this.commonName = commonName;
    }

    public double getMaxWeight() {
        return maxWeight;
    }

    public void setMaxWeight(double maxWeight) {
        this.maxWeight = maxWeight;
    }

    public String getIucnStatus() {
        return iucnStatus;
    }

    public void setIucnStatus(String iucnStatus) {
        this.iucnStatus = iucnStatus;
    }
    
    
    
}
